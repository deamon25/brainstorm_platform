"""CELL 12 — Attention Layer"""

class Attention(nn.Module):

    def __init__(self, hidden_dim):
        super().__init__()

        self.attn = nn.Linear(hidden_dim,1)

    def forward(self, x):

        weights = torch.softmax(self.attn(x), dim=1)

        context = (x * weights).sum(dim=1)

        return context

"""CELL 13 — Strong CNN + BiLSTM Model"""

class HesitationModel(nn.Module):

    def __init__(self, input_size=92):  # pass your actual feature count
        super().__init__()

        self.conv1 = nn.Conv1d(input_size, 64, 3, padding=1)  # ← fix
        self.conv2 = nn.Conv1d(64, 128, 3, padding=1)

        self.relu = nn.ReLU()

        self.lstm = nn.LSTM(
            input_size=128,
            hidden_size=128,
            batch_first=True,
            bidirectional=True
        )

        self.attention = Attention(256)
        self.fc = nn.Linear(256, 2)

    def forward(self, x):
        # x: [batch, seq_len, features] e.g. [16, 216, 92]
        x = x.permute(0, 2, 1)          # → [batch, features, seq_len]

        x = self.relu(self.conv1(x))    # → [batch, 64, seq_len]
        x = self.relu(self.conv2(x))    # → [batch, 128, seq_len]

        x = x.permute(0, 2, 1)          # → [batch, seq_len, 128]

        lstm_out, _ = self.lstm(x)      # → [batch, seq_len, 256]

        x = self.attention(lstm_out)
        x = self.fc(x)
        return x

