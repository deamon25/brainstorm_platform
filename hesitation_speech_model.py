"""CELL 6 — Advanced Feature Extraction"""

import librosa
import numpy as np

def extract_features(audio, sr):

    # -----------------------------
    # 1 Normalize audio
    # -----------------------------
    audio = librosa.util.normalize(audio)

    # -----------------------------
    # 2 Ensure fixed 5 second length
    # -----------------------------
    max_len = sr * 5

    if len(audio) > max_len:
        audio = audio[:max_len]
    else:
        pad = max_len - len(audio)
        audio = np.pad(audio, (0, pad))

    # -----------------------------
    # 3 Extract MFCC
    # -----------------------------
    mfcc = librosa.feature.mfcc(
        y=audio,
        sr=sr,
        n_mfcc=40
    )

    # -----------------------------
    # 4 Delta MFCC (speech dynamics)
    # -----------------------------
    delta = librosa.feature.delta(mfcc)

    # -----------------------------
    # 5 Chroma Features (pitch info)
    # -----------------------------
    chroma = librosa.feature.chroma_stft(
        y=audio,
        sr=sr
    )

    # -----------------------------
    # 6 Stack all features
    # -----------------------------
    features = np.vstack([mfcc, delta, chroma])

    # -----------------------------
    # 7 Force fixed frame width
    # -----------------------------
    max_frames = 216

    if features.shape[1] > max_frames:
        features = features[:, :max_frames]
    else:
        pad_width = max_frames - features.shape[1]
        features = np.pad(features, ((0,0),(0,pad_width)))

    return features.T  # ← ADD .T HERE → shape becomes [216, 92]

