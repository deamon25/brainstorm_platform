"""CELL 5 — Hesitation Labels"""

import re

fillers = ["um","uh","er","ah","hmm","like",
    "you know","i mean","sort of","kind of",
    "well","actually","basically"]

def label_text(text):

    text = text.lower()

    words = re.findall(r"\b\w+\b", text)

    for w in words:
        if w in fillers:
            return 1

    return 0


